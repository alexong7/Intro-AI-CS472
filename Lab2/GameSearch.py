# Define our cache for alpha beta
def cache1(function):
    "Like lru_cache(None), but only considers the first argument of function."
    cache2 = {}
    def wrapped(x, *args):
        if x not in cache2:
            cache2[x] = function(x, *args)
        return cache2[x]
    return wrapped

